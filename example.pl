% Rule to recommend clothing items with at least 1 matching attribute (excluding Style, AgeGroup)
recommend_clothing_min1(Style, AgeGroup, BodyType, FaceShape, SkinTone, Gender, Category) :-
    % Filter clothing items based on selected Gender and Category
    findall([Item, ImagePath, MatchCount], 
            (clothing(Item, StyleDB, AgeGroupDB, BodyTypeDB, FaceShapeDB, SkinToneDB, GenderDB, CategoryDB, ImagePath),
             StyleDB = Style,          % Match the Style exactly
             AgeGroupDB = AgeGroup,    % Match the AgeGroup exactly
             CategoryDB = Category,    % Match the Category exactly
             (Gender = 'Other' ; GenderDB = Gender),  % Match Gender if not 'Other'
             count_matches([BodyType, FaceShape, SkinTone, Gender, Category], 
                           [BodyTypeDB, FaceShapeDB, SkinToneDB, GenderDB, CategoryDB], 
                           MatchCount),
             MatchCount >= 1),  % At least 1 feature matches (excluding Style and AgeGroup)
            Matches),
    remove_duplicates(Matches, UniqueMatches),  % Remove duplicate items
    sort_matches_by_count(UniqueMatches, SortedMatches),  % Sort by match count
    display_sorted_matches(SortedMatches).  % Display the sorted matches

% Helper rule to remove duplicates based on item
remove_duplicates(List, UniqueList) :-
    sort(1, @<, List, UniqueList).  % Sort by the first element (Item) to remove duplicates

% Helper rule to count matches across all 7 features
count_matches([], [], 0).  % No more features to compare
count_matches([Feature|UserFeatures], [Feature|DBFeatures], MatchCount) :-
    count_matches(UserFeatures, DBFeatures, SubCount),
    MatchCount is SubCount + 1.  % If the feature matches, increment the count
count_matches([_|UserFeatures], [_|DBFeatures], MatchCount) :-
    count_matches(UserFeatures, DBFeatures, MatchCount).  

% Helper rule to sort matches by count (highest to lowest)
sort_matches_by_count(Matches, SortedMatches) :-
    sort(2, @>=, Matches, SortedMatches).  % Sort by the second element (MatchCount) in descending order

% Helper rule to display sorted matches based on 7 matching features
display_sorted_matches([]).
display_sorted_matches([[Item, ImagePath, 7]|Rest]) :-  % All 7 features match
    format('Highly recommended! Item: ~w, Image: ~w, Matches: 7~n', [Item, ImagePath]),
    display_sorted_matches(Rest).
display_sorted_matches([[Item, ImagePath, MatchCount]|Rest]) :-
    format('Item: ~w, Image: ~w, Matches: ~w~n', [Item, ImagePath, MatchCount]),
    display_sorted_matches(Rest).
